# Faça um programa que peça dois numeros inteiros. 
# Imprima a soma desses dois numeros na tela

print("Solicitação de valores e sua soma")
numero1 = int(input("Por favor, insira o primeiro valor: "))
numero2 = int(input("Por favor, insira o segundo valor: "))
soma = numero1 + numero2
print(f"O valor da soma é: {soma}")
